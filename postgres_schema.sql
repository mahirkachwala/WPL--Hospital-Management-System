-- PostgreSQL schema for Hospital Management System
-- Create database hospital_hms first in pgAdmin, then run this script inside that database.

DROP VIEW IF EXISTS view_current_appointments CASCADE;
DROP VIEW IF EXISTS completed_appointments CASCADE;
DROP VIEW IF EXISTS active_appointments CASCADE;

DROP TABLE IF EXISTS payment_transactions CASCADE;
DROP TABLE IF EXISTS prescriptions CASCADE;
DROP TABLE IF EXISTS appointment_transfers CASCADE;
DROP TABLE IF EXISTS past_appointments CASCADE;
DROP TABLE IF EXISTS current_appointments CASCADE;
DROP TABLE IF EXISTS tblpatient CASCADE;
DROP TABLE IF EXISTS patients CASCADE;
DROP TABLE IF EXISTS doctorslog CASCADE;
DROP TABLE IF EXISTS userlog CASCADE;
DROP TABLE IF EXISTS doctors CASCADE;
DROP TABLE IF EXISTS doctorspecialization CASCADE;
DROP TABLE IF EXISTS users CASCADE;
DROP TABLE IF EXISTS admin CASCADE;

DROP TYPE IF EXISTS payment_option_enum CASCADE;
DROP TYPE IF EXISTS patient_type_enum CASCADE;

CREATE TYPE payment_option_enum AS ENUM ('PayNow', 'PayLater', 'BookOnly');
CREATE TYPE patient_type_enum AS ENUM ('consultancy', 'admitted', 'emergency');

CREATE OR REPLACE FUNCTION set_updationdate()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updationdate = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION set_updatedat()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updatedat = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TABLE admin (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username varchar(255) NOT NULL,
    password varchar(255) NOT NULL,
    updationdate varchar(255) NOT NULL
);

CREATE TABLE users (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fullname varchar(255),
    address text,
    city varchar(255),
    gender varchar(255),
    email varchar(255),
    password varchar(255),
    regdate timestamp DEFAULT CURRENT_TIMESTAMP,
    updationdate timestamp
);

CREATE TABLE userlog (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uid integer,
    username varchar(255),
    userip varchar(45),
    logintime timestamp DEFAULT CURRENT_TIMESTAMP,
    logout varchar(255),
    status integer
);

CREATE TABLE doctorspecialization (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    specialization varchar(255),
    creationdate timestamp DEFAULT CURRENT_TIMESTAMP,
    updationdate timestamp
);

CREATE TABLE doctors (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    specialization integer,
    doctorname varchar(255),
    address text,
    docfees varchar(255),
    contactno bigint,
    docemail varchar(255),
    password varchar(255),
    creationdate timestamp DEFAULT CURRENT_TIMESTAMP,
    updationdate timestamp,
    CONSTRAINT fk_specialization FOREIGN KEY (specialization) REFERENCES doctorspecialization(id)
);

CREATE TABLE doctorslog (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uid integer,
    username varchar(255),
    userip varchar(45),
    logintime timestamp DEFAULT CURRENT_TIMESTAMP,
    logout varchar(255),
    status integer
);

CREATE TABLE patients (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    userid integer NOT NULL,
    doctorid integer,
    patientname varchar(255) NOT NULL,
    patientemail varchar(255),
    patientphone bigint,
    patientgender varchar(50),
    patientage integer,
    patientaddress text,
    patienttype patient_type_enum DEFAULT 'consultancy',
    status varchar(30) DEFAULT 'Active',
    isemergency smallint DEFAULT 0,
    admissiondate timestamp,
    dischargedate timestamp,
    notes text,
    createdat timestamp DEFAULT CURRENT_TIMESTAMP,
    updatedat timestamp
);

CREATE TABLE tblpatient (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    docid integer,
    patientname varchar(200),
    patientcontno bigint,
    patientemail varchar(200),
    patientgender varchar(50),
    patientadd text,
    patientage integer,
    creationdate timestamp DEFAULT CURRENT_TIMESTAMP,
    updationdate timestamp
);

CREATE TABLE current_appointments (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    doctorspecialization varchar(255),
    doctorid integer,
    userid integer,
    patientid integer,
    consultancyfees integer,
    appointmentdate varchar(255),
    appointmenttime varchar(255),
    postingdate timestamp DEFAULT CURRENT_TIMESTAMP,
    userstatus integer,
    doctorstatus integer,
    visitstatus varchar(30) NOT NULL DEFAULT 'Scheduled',
    checkintime timestamp,
    checkouttime timestamp,
    prescription text,
    paymentstatus varchar(20) NOT NULL DEFAULT 'Pending',
    paymentref varchar(64),
    paidat timestamp,
    appointmenttype varchar(50) DEFAULT 'Online',
    paymentoption payment_option_enum DEFAULT 'PayLater',
    updationdate timestamp
);

CREATE TABLE past_appointments (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    doctorspecialization varchar(255),
    doctorid integer,
    userid integer,
    patientid integer,
    consultancyfees integer,
    appointmentdate varchar(255),
    appointmenttime varchar(255),
    postingdate timestamp DEFAULT CURRENT_TIMESTAMP,
    userstatus integer,
    doctorstatus integer,
    visitstatus varchar(30) NOT NULL DEFAULT 'Scheduled',
    checkintime timestamp,
    checkouttime timestamp,
    prescription text,
    paymentstatus varchar(20) NOT NULL DEFAULT 'Pending',
    paymentref varchar(64),
    paidat timestamp,
    appointmenttype varchar(50) DEFAULT 'Online',
    updationdate timestamp,
    originalappointmentid integer NOT NULL DEFAULT 0,
    sourcetable varchar(64) NOT NULL DEFAULT '',
    archivedat timestamp
);

CREATE TABLE appointment_transfers (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    originalappointmentid integer NOT NULL,
    transferredappointmentid integer,
    patientid integer NOT NULL,
    doctorid integer NOT NULL,
    fromtype varchar(50) DEFAULT 'consultancy',
    totype varchar(50) DEFAULT 'admitted',
    transferreason varchar(255),
    transferdate timestamp DEFAULT CURRENT_TIMESTAMP,
    transferredat timestamp DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE prescriptions (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    patient_id integer NOT NULL,
    doctor_id integer NOT NULL,
    appointment_id integer NOT NULL,
    temperature varchar(20),
    blood_pressure varchar(30),
    pulse varchar(20),
    weight varchar(20),
    symptoms text,
    diagnosis text,
    tests text,
    notes text,
    medicines text,
    patient_name varchar(255),
    doctor_name varchar(255),
    next_visit_date date,
    created_at timestamp DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE payment_transactions (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    appointmentid integer NOT NULL,
    userid integer NOT NULL,
    patientid integer,
    amount decimal(10,2) NOT NULL,
    paymentref varchar(64),
    paymentstatus varchar(20) DEFAULT 'Pending',
    paymentmethod varchar(50) DEFAULT 'Online',
    transactiondate timestamp,
    createdat timestamp DEFAULT CURRENT_TIMESTAMP,
    updatedat timestamp
);

CREATE INDEX idx_users_email ON users(email);

CREATE INDEX idx_past_appointments_userid ON past_appointments(userid);
CREATE INDEX idx_past_appointments_doctorid ON past_appointments(doctorid);
CREATE INDEX idx_patients_userid ON patients(userid);
CREATE INDEX idx_patients_doctorid ON patients(doctorid);
CREATE INDEX idx_prescriptions_appointment_id ON prescriptions(appointment_id);
CREATE INDEX idx_prescriptions_patient_id ON prescriptions(patient_id);
CREATE INDEX idx_prescriptions_doctor_id ON prescriptions(doctor_id);
CREATE INDEX idx_payment_transactions_appointmentid ON payment_transactions(appointmentid);


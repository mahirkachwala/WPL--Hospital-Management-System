CREATE OR REPLACE VIEW active_appointments AS
SELECT *
FROM current_appointments
WHERE userstatus = 1
  AND doctorstatus = 1
  AND COALESCE(visitstatus, 'Scheduled') IN ('Scheduled', 'Checked In');

CREATE OR REPLACE VIEW completed_appointments AS
SELECT id, doctorspecialization, doctorid, userid, patientid, consultancyfees, appointmentdate, appointmenttime,
       postingdate, userstatus, doctorstatus, visitstatus, checkintime, checkouttime, prescription,
       paymentstatus, paymentref, paidat, appointmenttype, updationdate
FROM past_appointments
WHERE visitstatus IN ('Completed', 'Cancelled')
   OR userstatus = 0
   OR doctorstatus = 0;

CREATE OR REPLACE VIEW view_current_appointments AS
SELECT ca.id,
       ca.doctorspecialization,
       ca.doctorid,
       ca.userid,
       ca.patientid,
       ca.consultancyfees,
       ca.appointmentdate,
       ca.appointmenttime,
       ca.postingdate,
       ca.userstatus,
       ca.doctorstatus,
       ca.visitstatus,
       ca.checkintime,
       ca.checkouttime,
       ca.prescription,
       ca.paymentstatus,
       ca.paymentref,
       ca.paidat,
       ca.appointmenttype,
       ca.paymentoption,
       u.fullname AS username,
       u.email AS useremail,
       d.doctorname AS doctorname
FROM current_appointments ca
LEFT JOIN users u ON ca.userid = u.id
LEFT JOIN doctors d ON ca.doctorid = d.id;

